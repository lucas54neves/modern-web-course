function exponenciacao(base, expoente) {
    return base ** expoente
}

console.log(`1 ^ 2 = ${exponenciacao(1, 2)}`)
console.log(`3 ^ 2 = ${exponenciacao(3, 2)}`)
console.log(`5 ^ 4 = ${exponenciacao(5, 4)}`)
console.log(`6 ^ 6 = ${exponenciacao(6, 6)}`)
console.log(`2 ^ 8 = ${exponenciacao(2, 8)}`)
console.log(`5 ^ 3 = ${exponenciacao(5, 3)}`)