/**
 * Principios importantes da POO
 *  1. Abstracao:
 *      Habilidade de concentrar nos aspectos essenciais
 *  de um contexto qualquer, ignorando caracteristicas menos
 *  importantes ou acidentais. Em modelagem orientada a objetos,
 *  uma classe eh uma abstracao de entidades existentes no
 *  dominio do sistema de software.
 *  2. Encapsulamento:
 *      Conceito de programacao orientada a objetos que liga
 *  os dados e funcoes. O encapsulamento mantem ambos seguros de
 *  interferencia externa e ma utilizacao. O encapsulamento de
 *  dados leva ao importante coneiro de ocultacao de dados,
 *  tornando as informacoes privadas apenas a quem as possui.
 *  3. Heranca (Prototype):
 *  === Privilegie sempre a composicao em relacao a heranca ===
 *  - Composicao eh a caracteristica de objetos conterem outros
 *  objetos em suas variaveis de instancia
 *      -> Relacionamentos 'possui-um'
 *  - Heranca eh a caracteristica de objetos se organizarem em uma
 *  hierarquia
 *      -> Relacionamentos 'eh-um-tipo-de' / 'eh-um'
 *  4. Polimorfismo
 *      Caracteristica quando duas ou mais classes distintas tem
 *  metodos de mesmo nome, de forma que uma funcao possa utilizar um
 *  objeto de qualquer uma das classes polimorficas, sem necessidade
 *  de tratar de forma diferenciada conforme a classe do objeto.
 *      -> Classes abstratas sao uma das formas de se implementar o
 *  polimorfismo, cujos metos sao declarados mas nao definidos
 */